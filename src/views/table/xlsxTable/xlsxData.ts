export const getXlsxData = (start: number, size: number = 5) => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      Math.random() > 0.3
        ? resolve({
            data: xlsxData.slice(start, start + size),
            total: xlsxData.length,
            page: Math.floor(start / size) + 1,
          })
        : reject('error')
    }, 1000)
  })
}

export const getAllData = () => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      resolve({
        data: xlsxData,
      })
    }, 1000)
  })
}
